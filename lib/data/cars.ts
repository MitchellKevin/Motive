export type FuelType = "petrol" | "diesel" | "hybrid" | "phev" | "electric";
export type BodyType = "hatchback" | "sedan" | "wagon" | "suv" | "coupe" | "minivan" | "crossover";
export type GarageSize = "compact" | "standard" | "large" | "street";

export interface Car {
  id: string;
  make: string;
  model: string;
  variant: string;
  year: number;
  bodyType: BodyType;
  fuelType: FuelType;

  // Physical dimensions (mm)
  length: number;
  width: number;
  height: number;
  seats: number;
  bootLiters: number;
  bootLitersMax: number; // seats folded

  // Performance
  horsePower: number;
  acceleration: number; // 0–100 km/h in seconds
  topSpeed: number;

  // Economy
  priceNew: number; // EUR
  fuelConsumption: number; // L/100km (or kWh/100km for EVs)
  evRange: number | null; // km WLTP

  // Yearly costs (based on 20,000 km/year, Netherlands estimates)
  yearlyInsurance: number;
  yearlyMaintenance: number;
  yearlyFuelCost: number; // at 20k km

  // Practical scores (0–10)
  reliabilityScore: number;
  familyScore: number;
  cityScore: number;
  highwayScore: number;
  runningCostScore: number;
  parkingScore: number;
  roadTripScore: number;
  performanceScore: number;
  comfortScore: number;
  evScore: number; // 0 if not EV/PHEV

  // Human insights
  tagline: string;
  pros: string[];
  cons: string[];

  // Image (Wikimedia Commons or public domain)
  imageUrl: string;
  color: string; // dominant image color for tinting
}

